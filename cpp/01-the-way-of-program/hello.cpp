#include <iostream>  // 引入头文件
using namespace std; // 使用 std 标准库

// 注释以 // 开始
// 本程序用于生成一个简单的输出
// 编译方式：
//       g++ hello.cpp -o hello
// 运行方式：
//       ./hello

int main() {
    // main 函数是程序运行的入口
    // 前缀的 int 指定函数必须返回整型值
    // cout 和 endl 由 std 引入
    // 分别代表打印函数和换行符
    cout << "Hello, world." << endl;
    // return 指定返回值
    return 0;
}